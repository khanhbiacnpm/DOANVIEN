namespace VNPT.Modules.ThongTinNhanVien
{
    partial class SangKien_NhanVien : PortalModuleBase, IActionable    
    {
        string strConn = ConfigurationManager.ConnectionStrings["HRM"].ConnectionString;
        private int idNV = 0;
        protected void Page_Load(object sender, EventArgs e)
        {
            if (Request.Params["idNV"] != "null" && Request.Params["idNV"] != "undefined")
                idNV = Convert.ToInt32(Request.Params["idNV"]);
            load_grid(idNV);
        }

        private void load_grid(int idnv)
        {
            DataTable tbl = SqlHelper.ExecuteDataset(strConn, "HRM_SANGKIEN_NHANVIEN_GET", idnv, 0).Tables[0];
            grid_sangkiennhanvien.DataSource = tbl;
            grid_sangkiennhanvien.DataBind();
        }
              
        #region Optional Interfaces
        public ModuleActionCollection ModuleActions
        {
            get
            {
                ModuleActionCollection Actions = new ModuleActionCollection();
                Actions.Add(this.GetNextActionID(), Localization.GetString(ModuleActionType.AddContent, this.LocalResourceFile), ModuleActionType.AddContent, "", "", this.EditUrl(), false, SecurityAccessLevel.Edit, true, false);
                return Actions;
            }
        }       
        #endregion    
    }
}
